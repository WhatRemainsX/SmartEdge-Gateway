/**
 * ============================================================================
 * CRC16 Modbus 校验算法实现
 * ============================================================================
 * 
 * CRC (Cyclic Redundancy Check) 是一种基于多项式的数据校验方法
 * Modbus 协议采用 CRC16 作为标准校验方式
 * 
 * 算法特点：
 *   - 多项式：0xA001（Modbus 标准）
 *   - 初值：0xFFFF
 *   - 处理顺序：LSB（最低有效位）优先
 *   - 返回值：16 位校验码
 * 
 * 工作原理：
 *   1. 初始化 CRC = 0xFFFF
 *   2. 对每个数据字节：
 *      - 将字节与 CRC 最低 8 位异或
 *      - 对 8 个比特位进行处理
 *      - 每比特检查最低位，根据结果选择右移或异或多项式
 *   3. 返回最终的 CRC 值
 * ============================================================================
 */

#include "crc16.h"

/**
 * crc16_modbus - 计算 Modbus CRC16 校验值
 * @data: 指向待校验数据的指针
 * @length: 数据字节数
 * 
 * 返回值：16 位 CRC 校验码
 *
 * 说明：
 *   - 如果 data 为 NULL 且 length > 0，返回 0（错误情况）
 *   - 如果 data 为 NULL 且 length = 0，返回 0xFFFF（空数据）
 */
uint16_t crc16_modbus(const uint8_t *data,size_t length)
{
    uint16_t crc = 0xFFFFU;  // CRC 初值：0xFFFF（Modbus 规定）
    size_t index;             // 外层循环计数器（字节索引）
    uint8_t bit;              // 内层循环计数器（比特索引）

    // 错误检查：指针为 NULL 但请求长度 > 0，返回 0
    if ((data == NULL) && (length > 0U)) {
        return 0U;
    }

    // ========== 外层循环：逐字节处理 ==========
    for (index = 0U; index < length; index++) {
        
        // [步骤 1] 将当前字节与 CRC 的最低 8 位异或
        // 作用：将数据字节"混入"CRC 值中
        // 原理：XOR 操作混合了数据和校验值的信息
        crc ^= data[index];

        // ========== 内层循环：逐比特处理 ==========
        // 对每个字节的 8 个比特分别进行处理
        for (bit = 0U; bit < 8U; bit++) {
            
            // [步骤 2] 检查 CRC 的最低位
            // (crc & 0x0001U)：按位与 0x0001，只保留最低位
            // != 0U：判断最低位是否为 1
            if ((crc & 0x0001U) != 0U) {
                
                // [情况 A] 最低位为 1 时：
                // 1. 右移 1 位 (crc >> 1U)
                // 2. 与多项式 0xA001 异或
                // 作用：这是 CRC 多项式运算的实现
                crc = (uint16_t)((crc >> 1U) ^ 0xA001U);
            } else {
                
                // [情况 B] 最低位为 0 时：
                // 仅右移 1 位，不进行多项式异或
                crc = (uint16_t)(crc >> 1U);
            }
        }
    }

    // 返回计算得到的 CRC16 校验值
    return crc;
}